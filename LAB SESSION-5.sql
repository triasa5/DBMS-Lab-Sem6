CREATE TABLE ZIPCODES(
ZIP NUMBER(7) PRIMARY KEY,
CITY VARCHAR2(20));

CREATE TABLE EMPLOYEES(
ENO NUMBER(4) PRIMARY KEY,
ENAME VARCHAR2(20),
ZIP NUMBER(7) REFERENCES ZIPCODE(ZIP),
HDATE DATE);

CREATE TABLE PARTS(
PNO NUMBER(4) PRIMARY KEY,
PNAME VARCHAR2(20),
QOH NUMBER(3),
PRICE NUMBER(6,2),
LVL NUMBER(2));

CREATE TABLE CUSTOMERS(
CNO NUMBER(4) PRIMARY KEY,
CNAME VARCHAR2(20), 
STREET VARCHAR2(30),
ZIP NUMBER(7) REFERENCES ZIPCODES(ZIP),
PHONE NUMBER(10));

CREATE TABLE ORDERS(
ONO NUMBER(4) PRIMARY KEY,
CNO NUMBER(4) REFERENCES CUSTOMERS(CNO),
ENO NUMBER(4) REFERENCES EMPLOYEES(ENO),
RECEIVED DATE,
SHIPPED DATE);

CREATE TABLE ODETAILS(
ONO NUMBER(4) REFERENCES ORDERS(ONO),
PNO NUMBER(4) REFERENCES PARTS(PNO),
QTY NUMBER(3),
CONSTRAINT PK_ODETAILS PRIMARY KEY(ONO,PNO));

